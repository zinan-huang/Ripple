"""Numerical validation of the Apéry 8-variable PIVP conifold rate.

This script simulates the polynomial 8-state system recorded in
Ripple/Number/ApreyBounded.lean and checks the numerical signature

    |rho(tau) - zeta(3)| <= K32 * |z1 - z(tau)|^(3/2),

where z1 = 17 - 12*sqrt(2).  The initial state is not guessed: it is
computed from the Apéry generating functions A(z)=sum a_n z^n and
B(z)=sum b_n z^n at z0=1/1000:

    alpha   = A'/A''
    sigma_A = A/A''
    beta    = B'/A''
    rho     = B''/A''
    sigma_B = B/A''
    w       = 1/A''.

Those identities are exactly the projective variables whose Riccati-type
system appears in the Lean scaffold.
"""

from __future__ import annotations

from dataclasses import dataclass
import os
from pathlib import Path

os.environ.setdefault("MPLCONFIGDIR", "/tmp/matplotlib")
os.environ.setdefault("XDG_CACHE_HOME", "/tmp")

import matplotlib

matplotlib.use("Agg")
import matplotlib.pyplot as plt
import numpy as np
from scipy.integrate import solve_ivp
from scipy.special import zeta

ZETA3 = float(zeta(3))
Z1 = float(17 - 12 * np.sqrt(2))
EPS = 1.0 / 100.0
OUT = Path("experiments/apery_8var_pivp.png")


@dataclass(frozen=True)
class RunResult:
    tau: np.ndarray
    y: np.ndarray
    slope: float
    intercept: float
    k32_estimate: float
    k32_tail_median: float
    fit_count: int


def apery_recurrence(n_max: int) -> tuple[np.ndarray, np.ndarray]:
    """Return Apéry a_n, b_n arrays as float64, generated by recurrence."""
    if n_max < 1:
        raise ValueError("n_max must be at least 1")
    a = np.zeros(n_max + 1, dtype=np.float64)
    b = np.zeros(n_max + 1, dtype=np.float64)
    a[0], a[1] = 1.0, 5.0
    b[0], b[1] = 0.0, 6.0
    for n in range(1, n_max):
        coeff = 34 * n**3 + 51 * n**2 + 27 * n + 5
        denom = (n + 1) ** 3
        a[n + 1] = (coeff * a[n] - n**3 * a[n - 1]) / denom
        b[n + 1] = (coeff * b[n] - n**3 * b[n - 1]) / denom
    return a, b


def eval_series_derivatives(coeffs: np.ndarray, z: float) -> tuple[float, float, float]:
    """Evaluate F, F', F'' for F(z)=sum coeffs[n] z^n."""
    n = np.arange(coeffs.size, dtype=np.float64)
    powers = z ** n
    f0 = float(np.dot(coeffs, powers))
    if coeffs.size <= 1:
        return f0, 0.0, 0.0
    f1 = float(np.dot(coeffs[1:] * n[1:], z ** (n[1:] - 1)))
    if coeffs.size <= 2:
        return f0, f1, 0.0
    f2 = float(np.dot(coeffs[2:] * n[2:] * (n[2:] - 1), z ** (n[2:] - 2)))
    return f0, f1, f2


def init_state_from_recurrence(z0: float = 1.0e-3, n_terms: int = 180) -> np.ndarray:
    """Compute the 8-state IC from Apéry generating-function derivatives."""
    a, b = apery_recurrence(n_terms)
    A, Ap, App = eval_series_derivatives(a, z0)
    B, Bp, Bpp = eval_series_derivatives(b, z0)
    if App <= 0:
        raise RuntimeError(f"A''(z0) should be positive, got {App}")
    alpha = Ap / App
    sigma_a = A / App
    beta = Bp / App
    rho = Bpp / App
    sigma_b = B / App
    w = 1.0 / App
    zeta_state = rho
    return np.array([z0, alpha, sigma_a, beta, rho, sigma_b, w, zeta_state], dtype=np.float64)


def polynomials(z: float) -> tuple[float, float, float, float]:
    p = z**2 - 34.0 * z**3 + z**4
    q = 3.0 * z - 153.0 * z**2 + 6.0 * z**3
    r = 1.0 - 112.0 * z + 7.0 * z**2
    s = -5.0 + z
    return p, q, r, s


def rhs(_tau: float, y: np.ndarray) -> np.ndarray:
    z, alpha, sigma_a, beta, rho, sigma_b, w, zeta_state = y
    p, q, r, s = polynomials(z)
    big_q = q + r * alpha + s * sigma_a
    return np.array(
        [
            p,
            p + q * alpha + r * alpha**2 + s * sigma_a * alpha,
            alpha * p + sigma_a * big_q,
            rho * p + beta * big_q,
            r * (rho * alpha - beta) + s * (rho * sigma_a - sigma_b) + 6.0 * w,
            beta * p + sigma_b * big_q,
            w * big_q,
            EPS * (rho - zeta_state),
        ],
        dtype=np.float64,
    )


def integrate_trajectory(
    z0: float = 1.0e-3,
    n_terms: int = 180,
    tau_max: float = 5000.0,
    n_eval: int = 2600,
) -> tuple[np.ndarray, np.ndarray]:
    y0 = init_state_from_recurrence(z0=z0, n_terms=n_terms)
    t_eval = np.linspace(0.0, tau_max, n_eval)
    sol = solve_ivp(
        rhs,
        (0.0, tau_max),
        y0,
        method="DOP853",
        t_eval=t_eval,
        rtol=2.0e-11,
        atol=1.0e-13,
    )
    if not sol.success:
        raise RuntimeError(sol.message)
    return sol.t, sol.y


def fit_three_halves(tau: np.ndarray, y: np.ndarray) -> RunResult:
    z = y[0]
    rho = y[4]
    dist = np.abs(Z1 - z)
    err = np.abs(rho - ZETA3)
    ratio = err / np.maximum(dist, 1.0e-200) ** 1.5

    # Fit only the conifold tail, but avoid floating-point floor values.
    mask = (
        (dist > 2.0e-8)
        & (dist < 2.0e-4)
        & (err > 1.0e-12)
        & np.isfinite(dist)
        & np.isfinite(err)
        & np.isfinite(ratio)
    )
    if mask.sum() < 8:
        mask = (
            (dist > 1.0e-9)
            & (dist < 1.0e-3)
            & (err > 1.0e-13)
            & np.isfinite(dist)
            & np.isfinite(err)
            & np.isfinite(ratio)
        )
    if mask.sum() < 3:
        raise RuntimeError("not enough finite tail samples for a log-log fit")

    slope, intercept = np.polyfit(np.log(dist[mask]), np.log(err[mask]), 1)
    tail = mask & (dist < np.quantile(dist[mask], 0.35))
    if tail.sum() < 3:
        tail = mask
    k32_estimate = float(np.nanmax(ratio[mask]))
    k32_tail_median = float(np.nanmedian(ratio[tail]))
    return RunResult(
        tau=tau,
        y=y,
        slope=float(slope),
        intercept=float(intercept),
        k32_estimate=k32_estimate,
        k32_tail_median=k32_tail_median,
        fit_count=int(mask.sum()),
    )


def make_plot(result: RunResult) -> None:
    tau = result.tau
    z = result.y[0]
    rho = result.y[4]
    zeta_state = result.y[7]
    dist = np.maximum(np.abs(Z1 - z), 1.0e-200)
    err = np.abs(rho - ZETA3)
    ratio = err / dist**1.5

    finite = np.isfinite(dist) & np.isfinite(err) & (dist > 0) & (err > 0)
    fig, axes = plt.subplots(3, 1, figsize=(8.5, 11.0))

    ax = axes[0]
    ax.plot(tau, rho, lw=1.6, label=r"$\rho(\tau)$")
    ax.plot(tau, zeta_state, lw=1.1, alpha=0.75, label=r"adapted $\zeta$ state")
    ax.axhline(ZETA3, color="black", ls="--", lw=1.0, label=r"$\zeta(3)$")
    ax.set_xlabel(r"$\tau$")
    ax.set_ylabel("value")
    ax.set_title(r"8-variable Apéry PIVP: $\rho(\tau) \to \zeta(3)$")
    ax.grid(True, ls=":", alpha=0.6)
    ax.legend(loc="best")

    ax = axes[1]
    ax.loglog(dist[finite], err[finite], ".", ms=3.0, label="trajectory samples")
    xline = np.geomspace(np.nanmin(dist[finite]), np.nanmax(dist[finite]), 200)
    ref = result.k32_tail_median * xline**1.5
    ax.loglog(xline, ref, "--", lw=1.2, label="slope 3/2 reference")
    fit_ref = np.exp(result.intercept) * xline**result.slope
    ax.loglog(xline, fit_ref, "-", lw=1.0, alpha=0.8, label=f"fit slope {result.slope:.3f}")
    ax.set_xlabel(r"$|z_1-z(\tau)|$")
    ax.set_ylabel(r"$|\rho(\tau)-\zeta(3)|$")
    ax.set_title("Conifold log-log rate check")
    ax.grid(True, which="both", ls=":", alpha=0.6)
    ax.legend(loc="best")

    ax = axes[2]
    ax.semilogx(dist[finite], ratio[finite], ".", ms=3.0)
    ax.axhline(result.k32_tail_median, color="black", ls="--", lw=1.0, label="tail median")
    ax.set_xlabel(r"$|z_1-z(\tau)|$ (decreases to the left)")
    ax.set_ylabel(r"$|\rho-\zeta(3)|/|z_1-z|^{3/2}$")
    ax.set_title(r"Estimated $K_{32}$ along the trajectory")
    ax.grid(True, which="both", ls=":", alpha=0.6)
    ax.legend(loc="best")

    fig.tight_layout()
    fig.savefig(OUT, dpi=160)


def main() -> None:
    tau, y = integrate_trajectory()
    result = fit_three_halves(tau, y)
    make_plot(result)

    z_final = y[0, -1]
    rho_final = y[4, -1]
    err_final = abs(rho_final - ZETA3)
    dist_final = abs(Z1 - z_final)
    verdict = "OK" if 1.35 <= result.slope <= 1.65 else "ANOMALOUS"

    print("Apéry 8-var PIVP numerical validation")
    print(f"  zeta(3) target             = {ZETA3:.16e}")
    print(f"  rho(T_max)                 = {rho_final:.16e}")
    print(f"  |rho(T_max)-zeta(3)|       = {err_final:.6e}")
    print(f"  |z1-z(T_max)|              = {dist_final:.6e}")
    print(f"  measured log-log slope     = {result.slope:.6f}  [{verdict}]")
    print(f"  fit sample count           = {result.fit_count}")
    print(f"  K32 max over fit window    = {result.k32_estimate:.6e}")
    print(f"  K32 tail median estimate   = {result.k32_tail_median:.6e}")
    if verdict != "OK":
        print("  WARNING: measured slope is not close to 1.5; inspect the plot and IC assumptions.")
    print(f"  plot saved                 = {OUT}")


if __name__ == "__main__":
    main()
